#include <nfs/dserv_impl.h>

#include <sys/sdt.h>
#include <sys/list.h>
#include <nfs/nfs4.h>
#include <sys/types.h>
#include <sys/stat.h>
#include <sys/conf.h>
#include <sys/ddi.h>
#include <sys/sunddi.h>
#include <sys/modctl.h>
#include <sys/model.h>
#include <nfs/ds.h>

static struct cb_ops dserv_cb_ops = {
	nodev,			/* open */
	nodev,			/* close */
	nulldev,		/* strategy */
	nulldev,		/* print */
	nodev,			/* dump */
	nodev,			/* read */
	nodev,			/* write */
	nodev,			/* ioctl */
	nodev,			/* devmap */
	nodev,			/* mmap */
	nodev,			/* segmap */
	nochpoll,		/* poll */
	ddi_prop_op,		/* cb_prop_op */
	0,			/* streamtab  */
	D_NEW | D_MP		/* Driver compatibility flag */
};

static struct dev_ops dserv_ops = {
	DEVO_REV,	/* devo_rev */
	0,		/* refcnt */
	nodev,		/* get_dev_info */
	nodev,		/* identify */
	nodev,		/* probe */
	nodev,		/* attach */
	nodev,		/* detach */
	nodev,		/* reset */
	&dserv_cb_ops,		/* driver operations */
	NULL,			/* bus operations */
	nodev			/* dev power */
};

/*
 * Module linkage information for the kernel.
 */
static struct modldrv modldrv = {
	&mod_driverops,		/* module type (this is a pseudo driver) */
	"Deprecated Parallel NFS Data Server Module",	/* name of module */
	&dserv_ops		/* driver ops */
};

static struct modlinkage modlinkage = {
	MODREV_1,
	(void *)&modldrv,
	NULL
};

int
_init(void)
{
	return (mod_install(&modlinkage));
}

int
_info(struct modinfo *modinfop)
{
	return (mod_info(&modlinkage, modinfop));
}

int
_fini(void)
{
	return (mod_remove(&modlinkage));
}
